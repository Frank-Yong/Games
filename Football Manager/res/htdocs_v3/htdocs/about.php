	<h2>Ghid CupaLigii.ro!</h2>
	<br/>
	Jucatorii pot ajunge la maxim 50 la calitati. In functie de valoarea la care se afla, vor figura cu negru, portocaliu si verde. Toti jucatorii se antreneaza in fiecare zi, de aceea este nevoie ca din cand in cand sa le mai dati cate o zi libera, sa-si revina la forma. Daca un jucator nu joaca mai multe meciuri, scade la moral si rezultatele se vor vedea in joc.
	<br/><br/>
	Un aspect important: aveti grija la salarii, acestea se platesc saptamanal! Un lot mare de jucatori poate duce la costuri importante cu salariile, asadar nu transferati fara sa aveti in vedere acest aspect. Un jucator poate fi concediat, insa cu plata de salarii compensatorii. Pentru a face rost de bani, apelati la sponsorii pe care-i gasiti in meniu la Club. Totodata, zilnic, puteti primi bani suplimentari de la sponsori, daca dati click pe bannere pentru a primi bonusul.
	<br/><br/>
	Ghid de cumparare jucatori. Sunt doua tipuri de jucatori, unii liberi de contract, pentru care se liciteaza salariul saptamanal, iar altii pentru care se liciteaza suma de transfer. Cautati jucatorii pe "verde", adica cei care au la caracteristicile principale valoare mare.
	<br/><br/>
	Ce caracteristici sunt importante:
	<br/>
	Portari: reflexe, comunicatie, unu la unu, manevrare, pozitionare.
	<br/>
	Fundasi: marcaj, deposedare, pozitionare, jocul cu capul
	<br/>
	Mijlocasi: jocul de pase, creativitate, lansari, suturi de la distanta, atingere
	<br/>
	Atacanti: sutul, jocul cu capul, viteza, pozitionare.
	<br/><br/>
	Stadion: poti mari capacitatea stadionului, astfel incat sa iei mai multi bani din biletele de intrare la meci. Totodata, in cazul in care joci cu casa plina, te ajuta atat financiar, cat si in meci. Ai grija la pretul la bilete, suporterii sunt pretentiosi si dau bani multi din buzunar doar daca merita!<br/><br/>
	Antrenor: pentru ca jucatorii sa creasca in calitate cat mai mult, angajeaza-ti antrenor. Antrenorul nu are rol doar la antrenamente, ci si in timpul meciului. Un antrenor il poti concedia fara probleme, dar ca sa-ti iei altul, trebuie sa platesti prima de instalare.
	<br/><br/>
	Transferuri: daca vrei sa transferi jucatori, mergi in meniu la Cautare, iar apoi la Jucatori. Aici gasesti toti jucatori aflati pe lista de transfer, fie ei liberi de contract, fie sub contract cu alt club. Odata ce s-a inceput licitatia, e timp 48h pentru orice sa parieze pe jucator. Daca se pariaza in ultimele minute, perioada de licitatie se mareste. Castiga cine are cea mai mare suma pariata in perioada de timp.
