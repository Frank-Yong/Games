	De asemenea, se poate stabili pretul la bilete in functie de oponent. Daca ratingul echipei adverse este ridicat, sansele ca spectatorii sa asiste la meci sunt mai mari, dar tot in functie de pretul de la bilete. Un alt factor important pentru prezenta spectatorilor pe stadion il reprezinta si vremea din ziua meciului.
