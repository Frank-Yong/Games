	<br/>
	Poti sa iti cresti capacitatea stadionului, pentru a incasa mai multi bani din bilete. Atentie, modernizarea stadionului face ca acel sector sa nu fie disponibil pentru o perioada destul de lunga de timp (minim 15 zile), in functie de numarul de locuri dorit. Nu trebuie trecuta cu vederea costul constructiei!
