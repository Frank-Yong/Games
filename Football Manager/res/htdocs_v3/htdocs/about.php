	<h2>Detalii jucator</h2>
	<br/>
	Pentru fiecare jucator in parte gasesti informatii suplimentare. Exista caracteristici specifice in fucntie de postul pe care un jucator evolueaza.	
	<br/><br/>
	De exemplu, pentru un atacant este foarte important sutul si jocul de cap. De asemenea, pozitionarea atacantului are un rol foarte important. Aceste caracteristici sunt marcate in detaliile jucatorului, iar in functie de cat de bun este, culorile difera: negru, portocaliu, verde (verde fiind cel mai bun).
	<br/><br/>
	Fiecare jucator are o valoare de piata si in functie de aceasta valoare, este platit cu un salariu corespunzator. Salariul se plateste saptamanal, banii retragandu-se din cont automat.
